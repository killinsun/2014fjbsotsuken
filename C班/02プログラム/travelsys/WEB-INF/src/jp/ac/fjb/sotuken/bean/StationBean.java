package jp.ac.fjb.sotuken.bean;

public class StationBean {

	private String stationId;
	private String stationname;
	private String stationhurigana;


	public String getStationId() {
		return stationId;
	}
	public void setStationId(String stationId) {
		this.stationId = stationId;
	}
	public String getStationname() {
		return stationname;
	}
	public void setStationname(String stationname) {
		this.stationname = stationname;
	}
	public String getStationhurigana() {
		return stationhurigana;
	}
	public void setStationhurigana(String stationhurigana) {
		this.stationhurigana = stationhurigana;
	}

}
