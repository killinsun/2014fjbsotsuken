public class KankouchiBean {
	private String kankochiId;
	private String kankochiName;
	private String kenId;


	public String getKankochiId() {
		return kankochiId;
	}
	public void setKankochiId(String kankochiId) {
		this.kankochiId = kankochiId;
	}
	public String getKankochiName() {
		return kankochiName;
	}
	public void setKankochiName(String kankochiName) {
		this.kankochiName = kankochiName;
	}
	public String getKenId() {
		return kenId;
	}
	public void setKenId(String kenId) {
		this.kenId = kenId;
	}
}
