package jp.ac.fjb.sotuken.bean;

public class KenBean {
	private String kenId;
	private String kenName;
	private String chiikiId;
	private String chiikiName;


	public String getKenId() {
		return kenId;
	}
	public void setKenId(String kenId) {
		this.kenId = kenId;
	}
	public String getKenName() {
		return kenName;
	}
	public void setKenName(String kenName) {
		this.kenName = kenName;
	}
	public String getChiikiId() {
		return chiikiId;
	}
	public void setChiikiId(String chiikiId) {
		this.chiikiId = chiikiId;
	}
	public String getChiikiName() {
		return chiikiName;
	}
	public void setChiikiName(String chiikiName) {
		this.chiikiName = chiikiName;
	}
}
